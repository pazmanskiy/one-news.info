<h2>Что представляет собой Окомарин и как он появился?</h2>
<p>Автором препарата является ни кто иной, как сам Святослав Федоров – тот самый знаменитый советский офтальмолог, глазной микрохирург, профессор, академик, герой Социалистического Труда СССР.</p>
