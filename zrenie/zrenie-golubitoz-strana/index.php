<p>Изначально разрабатываемый учеными препарат назывался Орел. Его разработка началась в 1991 году на базе ГУ «Институт имени В.П. Филатова». Ученым потребовалось почти 8 лет, чтобы создать препарат, который в значительной мере улучшал зрение без операции за счет повышения тонуса глазных мышц и очистки от помутнения хрусталика. Препарат мог использоваться для лечения таких глазных заболеваний, как:</p>
