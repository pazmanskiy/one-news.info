<p>После смерти ученого сотрудникам и Минздраву было не до нового препарата, который к тому моменту уже заканчивал серию клинических испытаний, доказавших его высокую эффективность. Началась дележка наследства и наследия великого ученого. Институт охватили интриги и «междоусобные войны». </p>
<p>Практически за гроши на тот момент (с учетом высокой эффективности препарата) его формулу выкупила Швейцарская офтальмологическая корпорация, которая за 2 года наладила его производство под другим названием. </p>
<p>Лишь только в 2015 году благодаря стараниям жены Федорова с большим трудом удалось отстоять патент и вернуть формулу обратно в страну. Однако само производство препарата претерпело некоторые изменения – вместо устаревшей технологии получения полезных веществ из растений стала использоваться новейшая технология холодной экстракции (которой еще не было в 90-х годах). Благодаря этому удалось повысить эффективность препарата еще на 47% (холодная экстракция сохраняет в 3-5 раз больше полезных биологически активных веществ из растений). Обновленный препарат стал называться Окомарин.</p>
