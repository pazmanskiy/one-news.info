                <h1>МАРИЯ К.:  Я мужа избавила от тяги к спиртному за 2 недели. Справилась сама!</h1>

                <div class="post_block">

                    <div class="post_date">Рубрика: <u>Нам пишут</u> | 19.06.2019 12:11:31</div>

                    <div class="post_content">
                        <p>Хочу поделиться своей историей, чтобы помочь тем женщинам, которые живут с пьющими мужьями и которые хотят вырвать своих благоверных из лап зеленого змея. Дорогие мои, не ставьте на них крест. Спасение возможно!
                        </p>

                        <h3><br>А теперь моя история</h3>

                        <p>Познакомились мы с Мишей три года назад. Ему было 40, мне 28. Он был разведен, для меня это был первый брак. Я и до свадьбы знала, что Миша любит выпить. И из-за этого распалась его первая семья. Но он клялся и божился, что сделал выводы, и будет держать себя в руках. Я ему поверила. </p>
                        <p>И правда, первый год мой муж пил нечасто – по праздникам, дням рождения, ну иногда с друзьями зависал. Словом, было терпимо. Я размечталась о ребенке. Точнее я о нем думала и до свадьбы, но решила посмотреть, что у меня получится с замужеством.</p>



                        <p><img src="img\202.jpg" alt="" class="center">
                        </p>
                        <blockquote>Сначала муж пил умеренно – по праздникам, иногда с друзьями или когда выезжали на природу</blockquote>



                        <p>Увы, с каждым месяцем Миша был нетрезвым все чаще и чаще. Появлялись все новые и новые поводы, все новые и новые друзья, точнее их уже можно было назвать собутыльниками. Начались скандалы. Но кроме потрепанных нервов они ни к чему не привели. Это я сейчас понимаю, что алкоголизм – это болезнь, которую НАДО ЛЕЧИТЬ, как лечат другие болезни – нужными лекарствами. А тогда я думала, что можно помочь душевными разговорами, слезами и криками. Муж клялся, что прекратит пить, но его обещаний хватало на максимум на неделю. А раз в месяц он уже напивался до беспамятства. Начались проблемы на работе. О ребенке я больше даже не думала…</p>

                        <p><img src="img\203.jpg" alt="" class="center">
                        </p>
                        <blockquote>Муж пил все чаще и чаще и уже трезвым бывал реже, чем пьяным</blockquote>

                        <h3><br>Как я пыталась ему помочь</h3>

                        <p>Я уговорила его сходить к врачу-наркологу. Тот прописал Мише какие-то таблетки, которые я через три дня нашла в мусорном ведре. Моя мама дала мне денег, и мы обратились к очень известному в Львове наркологу, который избавляет от тяги к спиртному гипнозом и иглоукалыванием. Целую неделю Миша под моим присмотром ходил к нему на сеансы. После этого лечения он держался почти два месяца. Я снова стала мечтать о ребенке. Но Миша запил снова. И еще сильнее, чем раньше. Однажды, когда он впервые на меня поднял руку, я наконец то осознала - мой муж алкоголик. </p>

                        <p><img src="img\206.jpg" alt="" class="center">
                        </p>
                        <blockquote>После того, как он первый раз меня ударил, я осознала, что мой муж - алкоголик</blockquote>


                        <h3><br>Как делать НЕ НУЖНО!</h3>

                        <p>Но вместо того, чтобы ЛЕЧИТЬ его, я стала пить вместе с ним. То есть допустила ошибку, которую совершают многие женщины. Смирившись, что не могут заставить своего мужа не пить, они решают, что пусть он хоть пьет дома, под присмотром. Но нужен же собутыльник. Вот таким собутыльником жены и становятся. </p>

                        <p>И знаете, вначале мне даже понравилось! Муж стал относиться ко мне внимательней. Реже зависал с друзьями по разным кабакам, а приходил домой. Даже первый месяц, когда я с ним начала пить, сам он пить стал меньше. Наверное, меня стеснялся. Но потом стесняться он перестал и пил почти каждый день. Естественно, с работы его быстро выгнали. Так что стали мы жить с ним на мою зарплату библиотекаря. А какие это деньги? 6 тысяч с копейками. Для двоих, да еще пьющих – это на неделю. Муж продал машину, которую мы пропили за полгода. </p>


                        <p><img src="img\204.jpg" alt="" class="center">
                        </p>


                        <p>С матерью я, конечно, рассорилась. Так мы прожили еще год, с тех пор, как я стала пить с мужем. Однажды на пустыре возле своего дома я увидела двух бомжей – мужчину и женщину, которые сидели прямо на траве и пили – грязные, с испитыми фиолетово-красными лицами. И тут мне стало страшно! Я увидела в руках бомжа бутылку водки, и мне захотелось выпить! Я поняла, что спиваюсь и если не остановлюсь, то мы с мужем превратимся в таких же бомжей. Машину продали. Продадим и квартиру!</p>


                        <h3><br>Спасение найдено!</h3>

                        <p>В испуге я прибежала к матери. Вдвоем мы поплакали, и она записала меня к тому знаменитому наркологу. Но и мне его лечение не помогло. Не знаю, чем бы закончилась вся эта история, точнее точно знаю – под каким-нибудь забором, если бы мама однажды не пришла ко мне, когда Миши не было. Ни слова ни говоря, она вытащила из сумки коробку с надписью <strong>«Нооклан»</strong>, открыла, вытащила бутылочку и накапала содержимое в стакан с водой. </p>

                        <p>- <strong>Пей!</strong> – приказала она. </p>

                        <p>Я выпила. На вкус, что-то нейтральное, сразу и не поймешь, что сейчас выпила. В дальнейшем, это свойство отсутсвия вкуса мне очень поможет! </p>

                        <p>- <strong>Будешь пить два раза в день, утром и вечером. И своему незаметно давай</strong>, - с этими словами она ушла. </p>

                        <p>Вечером завалился домой муж, естественно, пьяный. А я неожиданно поняла, что вечером хлопнуть 100 грамм меня что-то не тянет!</p>

                        <p>Утром, я снова выпила Нооклан, а Мише накапала в банку с рассольчиком, который у нас постоянно стоял в холодильнике. Но сначала отлила из нее с таким расчетом, чтобы оставшиеся он все выпил. Вот так незаметно, под разными «соусами» я стала давать ему Нооклан утром и вечером. </p>

                        <p>Нооклан на меня подействовал сразу. Дня через три я на водку смотрела с отвращением. Чтобы Миша был под моим присмотром я пошла на хитрость – сказала, что плохо себя чувствую по-женски, врачи приписали лекарства и 2 недели запретили принимать спиртное. Но за компанию, я с ним посижу. Через неделю я увидела, что Нооклан стал действовать и на мужа. Он стал меньше пить, а еще через несколько дней вечером вообще отказался от водки!! </p>

                        <p>На следующий день новая моя радость. Миша по привычке утром хлопнул 100 грамм, как он говорил для разгона, съел супчика (с чудодейственными каплями) и его вырвало! Вечером снова насыпала и снова рвота!</p>

                        <p>- <strong>Прекращал бы ты! Уже твой организм говорит, что не может принимать. Залит под завязку. Помереть хочешь?</strong> </p>

                        <p>С тех пор Миша в рот спиртного не брал. А еще через пару дней признался мне:</p>

                        <p>- <strong>Знаешь, Маш, точно я, наверное, всю свою норму выбрал. Вообще пить не хочется. Даже думать об этом противно!</strong>
                        </p>

                        <p>Но я ему, на всякий случай, Нооклан этот продолжала давать, пока вторая неделя не закончилась (мать потом еще принесла, увидев, как средство помогает). </p>

                        <p>Миша, когда перестал пить, сильно изменился. Прежде всего, внутренне. Его стал интересовать окружающий мир! Так, конечно, совпало, но в тот день, когда Миша выпил последне капли Нооклан, он устроился на работу на старое место – оператором ЧПУ на авиационный завод, а в субботу пригласил меня на вечернюю прогулку и в кино.</p>


                        <p><img src="img\201.jpg" alt="" class="center">
                        </p>
                        <blockquote>Перестав пить муж изменился</blockquote>



                        <p>Прошло полгода. Миша не пьет. АБСОЛЮТНО. Ни по праздникам. Ни по будням. НИКОГДА. Теперь он изменился и внешне. Исчезла опухлость на лице, пропал туповатый взгляд. Словом, стал мужчиной в расцвете сил!</p>

                        <p>В этом году мы были в Турции, и там Миша узнал две новости. Во-первых, что я беременна. А во-вторых, про Нооклан. Что было дальше? Дальше был чудесный отдых!</p>

