                        <h4>История современной Золушки или как сбросить 30 килограмм</h4>
                        <h4><span style="color: rgb(0, 166, 221);">Предыстория</span></h4>
                        <p>Судьба бывает разной. Одним Бог дает тонкую талию и длинные ноги, другим большие килограммы. Но
                            дает Бог и возможность все изменить. Одну такую историю я хочу рассказать – про себя.</p>
                        <p><b>Сколько себя помню, я была пышечка</b>. В садике, в школе. Пампушкой поступила и на филфак
                            университета им. Т. Шевченка. Мне даже нравилось быть такой полненькой, крепенькой, сбитой, в которой
                            есть за что ухватиться. Нравилось это и мужчинам – в 19 лет выскочила я замуж за своего
                            одноклассника, через год родила. Беременность обошлась мне очень дорого. Мое тело полезло как
                            квашня. Стрелка весов постоянно обновляла рекорд, как курс гривны к доллару. В итоге при
                            рождении сына я весила – 87 кг! Или плюс 13 кг к стартовому весу.
                        </p>
                        <blockquote>
                            <h4>Беременность обошлась мне очень дорого. Я набрала 13 кг и стала весить 87 кг</h4>
                        </blockquote>
                        <p style="text-align: center;">
                            <img class="size-img" alt="Istoriya_pohudeniya_Kochina5" src="1.jpg" width="400">
                        </p>
                        <p>
                            А дальше, кормление грудью, другие бесконечные хлопоты за ребенком, за которыми я не заметила,
                            как за год набрала еще 10 кг. Итого 97 кг. Удручающая цифра.
                        </p>
                        <blockquote>
                            <h4>За хлопотами молодой мамы я и не заметила, как за год набрала еще 10 кг – удручающая
                                цифра</h4>
                        </blockquote>
                        <p style="text-align: center;">
                            <img class="size-img" alt="Istoriya_pohudeniya_Kochina5" src="3.jpg" width="500">
                        </p>
                        <p>В университет после академотпуска вернулась не пышечка, а самая настоящая корова. И я даже не
                            заметила, как еда стала для меня самоцелью, а завтраки, обеды, ужины и многочисленные перекусы
                            чем-то большим, нежели просто удовлетворение инстинкта голода. Это стали самые приятные и
                            желанные ритуалы. </p>
                        <blockquote>
                            <h4>Я и не заметила, как еда стала для меня самоцелью, моим самым желанным
                                времяпровождением</h4>
                        </blockquote>
                        <p style="text-align: center;">
                            <img class="size-img" alt="Istoriya_pohudeniya_Kochina5" src="5.jpg" width="400">
                        </p>
                        <p>Даже муж отошел на второй план. Я спохватилась, но было поздно – мой Коля, потерпев какое-то
                            время, отправился искать себе другую женщину.
                        </p>
                        <blockquote>
                            <h4>Муж не вытерпел и отправился искать себе другую женщину</h4>
                        </blockquote>
