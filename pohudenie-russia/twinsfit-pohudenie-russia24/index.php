							врачи повсеместно рекомендуют отказываться от высококалорийной пищи и побольше заниматься
							физической деятельностью. Вторая причина — нарушение обмена веществ. В человеческом организме
							начинают продуцировать биохимические реакции не так, как они должны быть изначально.</p>





