							использовать для физической и умственной активности, наш организм превращает ее в жир. Как
							результат – человек становится вялым — как физически, так и умственно. У него растет вес, а
							внутренние органы буквально купаются в жире. Проблема усугубляется еще и тем, что переедание часто,
							в конце концов, приводит к нарушению обмена веществ. То есть мы полнеем и потому что передаем, и
							потому что у нас нарушен обмен веществ. А лишний вес – это главный провокатор сердечно-сосудистых
