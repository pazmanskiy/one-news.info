<p>Ранее считалось, что диабет полностью вылечить практически нельзя. Так вот его методика позволяет стабилизировать сахар навсегда. То есть избавиться от диабета. Как раз его лекарство мы использовали для лечения Заворотнюк. И именно из-за него она пришла в сознание, после того, как мы стабилизировали сахар в крови до нормальных значений. Ведь кома в которую впала Анастасия, не была напрямую связана с раком. Это была гиперосмолярная некетоновая кома, которая является классическим осложнениям сахарного диабета. При помощи нового препарата мы привели ее в сознание, снизив сахар с 12.5 ммоль/л до нормы. Если бы не это, Заворотнюк бы уже никогда не очнулась.</p> 
              
              
              
              
