<p><b>Корреспондент: "Ваш препарат вытащил Заворотнюк из комы. Как такое возможно?"</b></p>  

<p>У нее была кома, связанная напрямую с диабетом. Ее основной причиной был крайне высокий уровень сахара в крови и многочисленные осложнения в работе внутренних органов. Все это осложнялось быстро прогрессирующей раковой опухолью головного мозга. Это был особенно сложный случай. К счастью, препарат сработал и в этом случае. Честно говоря, я отчасти сомневался, что тут он поможет. Слишком много всего наложилось — повреждение сразу нескольких внутренних органов, зашкаливающий сахар, прогрессирующая раковая опухоль. Но Заворотнюк очнулась и это главное.</p> 

<p><b>Корреспондент: "Николай Тронько назвал ваше открытие в области лечения диабета новой главой в истории медицины, а неделю назад вашу кандидатуру подали на Нобелевскую премию по медицине. Можете кратко объяснить, чем ваш вариант лечения диабета отличается от старых методов?"</b></p>  

<p>Не уверен, что получится коротко объяснить разницу, но я попробую. Старые методы лечения диабета в своем большинстве основаны на принципе принудительной стимуляции или на компенсации. В первом случае, в организм вводится действующее вещество, которое выступает в качестве раздражителя и буквально силой заставляет организм расщеплять сахар. Если говорить более конкретно, то они воздействуют на "инсулиновые рецепторы" и заставляют их более активно работать. Либо препятствует всасыванию сахара в кровь из желудка. По задумке врачей, это должно было обеспечить стабильный уровень сахара. На момент, когда создавались препараты с таким подходом, они были настоящим прорывом в лечении диабета. Проблема только в том, что это было 60-70 лет назад.</p> 

<p><b>Корреспондент: "Что происходит с организмом при регулярном приеме подобных препаратов?"</b></p>
<p>Организм за счет внешней стимуляции какое-то время удерживает сахар в нормальных параметрах. Но если постоянно заставлять какую-то деталь работать на износ, она придет в негодность. Собственно так происходит и здесь. В итоге диабетику требуются более серьезные дозы или другие препараты, с более мощным воздействием. Как правило, после какого-то промежутка времени, он и вовсе пересаживается на уколы инсулина. Это значит, что организм уже дышит на ладан и шансов выжить у человека немного. </p> 

<p>Безусловно, есть и более продвинутые варианты лечения диабета. Но, как правило, они проводятся в условиях стационара и предлагают их не более пары десятков клиник по всему миру. Стоимость такого комплекса лечения начинается от 100-120 тысяч долларов и доходил до полумиллиона, в зависимости от тяжести ситуации. Понятно, что так лечить диабет могут только крайне состоятельные люди.</p> 

<p>Что касается моего метода лечения диабета, то он ориентирован на естественное восстановление выработки и усвоения инсулина. Как правило, у диабетиков наблюдаются две проблемы — снижение чувствительности тканей к инсулину и нарушение работы секреции по выработке инсулина. Соответственно, чтобы вылечиться, нужно решить обе проблемы. Я сумел подобрать набор компонентов, которые на это способны. Во-первых, они восстанавливают работу секреции по выработке инсулина, во-вторых, возвращают чувствительность инсулиновым рецепторам. Что важно, не принудительно заставляют их работать лучше, а делают это естественным путем.</p> 

<p><b>Корреспондент: "И это помогает вылечить диабет?"</b></p>  

<p>Это полностью восстанавливает естественный цикл выработки и усваивания инсулина в организме. В переводе с медицинского — да, это позволяет полностью вылечить диабет. Человек может больше не волноваться о повышенном уровне сахара. Дополнительные элементы, входящие в состав препарата, положительно воздействуют на поврежденные человеческие органы. Прекращаются процессы некроза, восстанавливаются поврежденные ткани, запускается процесс регенерации сосудов. Организм откатывается назад, на более здоровый уровень.</p>  

<p><b>Корреспондент: "Лекарство, созданное на основе вашего открытия, насколько я знаю, уже прошло клинические испытания?" </b></p>  

<p>Да, около 3 месяцев назад завершились клинические испытания препарата. Ради справедливости, хочу сказать, что если разработкой методики я занимался сам, то над созданием препарата работала масса людей из разных структур, от НАН до специалистов из Карпатского НИИ Эндокринологии. Это была большая команда, более чем из сотни специалистов. Чтобы собрать все компоненты вместе, создать устойчивую формулу и "упаковать" все это в капсулы, потребовались колоссальные усилия. Один я мог бы заниматься этой задачей еще лет десять. </p> 

<p>В клинических испытаниях НООБЕТ показал отличные результаты. Из 1000 человек, которые пришли на испытания с тяжелыми и запущенными формами диабет, он смог полностью вылечить 912 человек. У оставшихся 88 наблюдались какие-то остаточные явления, как например незначительные повышения сахара в критических ситуациях, например при употреблении в пищу очень большого количества сладкого. Но если сравнивать с тем состоянием, в котором они начинали лечение, это тоже отличный результат. Именно после результатов клинических испытаний, мне удалось убедить медицинское руководство в необходимости  выдачи препарата.</p> 

<p><b>Корреспондент: "В каком смысле ?"</b></p>  

