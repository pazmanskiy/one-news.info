<p> Доброго дня. Сейчас Анастасия действительно пришла в сознание, хотя ее состояние продолжает оставаться крайне тяжелым. Нам удалось буквально вытащить ее с того света, и около 8 часов назад она впервые за последние несколько дней открыла глаза. Мы прилагаем все усилия, чтобы спасти ее. Но на данный момент времени еще рано делать прогнозы, пока абсолютно непонятно, как сложится ситуация. Хотя, то что она пришла в сознание, естественно является хорошим знаком.</p>

              
              
<p><b>Корреспондент: "Как у вас это получилось? До этого рак стремительно прогрессировал и казалось, что его уже не остановить."</b></p>  

<p>Надо сразу сказать, что мы пока и не остановили рак. Затормозили метастазы — да, улучшили состояние пациента — тоже да. Но раковая опухоль никуда не делась, как и ее последствия. И сейчас предстоит масса тяжелой работы. Я не могу сказать, что у нас точно получится ее спасти, но не могу утверждать и обратного. Ситуация меняется очень быстро. Прямо сейчас мы наблюдаем определенное улучшение и надеюсь, ухудшаться состояние Анастасии не будет.</p> 

<p>Я давно знаком с Анастасией и еще несколько лет назад говорил ей, что нужно лечиться. Мало кто знает, но у нее уже достаточно давно был диабет. Я сам по второй специализации эндокринолог, специализируюсь на лечении диабетиков с раковыми опухолями. И прекрасно понимаю, какую опасность несет сахарный диабет. Еще тогда, несколько лет назад, были эффективные варианты лечения болезни. К сожалению, не в Украине, за рубежом. Но Анастасия могла воспользоваться этими вариантами. А вместо этого, она предпочитала глотать таблетки, порой вообще переходя на уколы инсулина. </p> 

<p><b>Корреспондент: "Я не совсем понимаю, как раковая опухоль связана с диабетом?"</b></p>  

<p>Напрямую. У Анастасии сахар часто подскакивал до 7.5-8.0 ммоль/л, а то и еще выше. Она его сбивала таблетками или уколами инсулина. Но сути дела это не меняло, каждый день в ее крови уровень сахара взлетал до критического. Что такое сахар в 7.5 ммоль/л? Это поражение внутренних органов, процессы некроза и гниения, изорванные стенки сосудов. Организм разрушается изнутри и это состояние быстро прогрессирует. </p> 

<p>В ее случае наблюдается сразу несколько патологий. Помимо раковой опухоли головного мозга, у нее была обнаружена почечная недостаточность и сердце в прединфарктном состоянии. А печень вообще наполовину сгнила. Сама же раковая опухоль появилась после того, как из-за сахара начался некроз в тканях головы. Изначальный очаг некроза, как мы определили, находился рядом с одним из кровеносных сосудов. Далее, на его базе сформировалась опухоль и начался ее быстрый рост. </p> 

<p>От таких последствий диабета нельзя спастись, просто глотая сахароснижающие таблетки или вкалывая инсулин. Оба этих варианта годятся, чтобы локализовать слишком критичный всплеск сахара, когда он доходит до 10-12 ммоль/л, но не годятся для лечения на постоянной основе. При регулярном применении, они скорее наоборот, опасны.</p> 

<p><b>Корреспондент: "Получается, Заворотнюк заболела раком, потому что неправильно лечила диабет?"</b></p>  

<p>Если говорить грубо, то да. Сначала она вообще не верила, что у нее сахарный диабет. Считала, что это все обычная усталость или недомогания. Насколько я знаю, это мать уговорила ее регулярно проводить замеры сахара. И по ним стало понятно, что она больна. Она же уговорила дочь прийти ко мне на прием. Но, к сожалению, тогда Анастасия Юрьевна отнеслась к своей болезни с долей скепсиса, мол миллионы же болеют диабетом и ничего, живут.</p> 

<p>Да, болеют десятки миллионов. И каждый год миллионы умирают в мучениях по всему миру. От рака внутренних органов, сепсиса, сердечной или почечной недостаточности, гипогликемии и массы иных причин. А на их место встают новые диабетики. Лечатся они в основном старинными методами, о которых я уже говорил — сахароснижающими таблетками и уколами инсулина. Хотя это сложно назвать лечением. Все равно что человека у которого гангрена ноги, лечить обезболивающими. Он будет чувствовать себя какое-то время хорошо, но гангрена никуда не денется. И как только пройдет действие одной таблетки, ему понадобится следующая. Вплоть до того момента, когда он умрет. По сути так же дело обстоит и с диабетиками. Такое лечение не может спасти. Поэтому сейчас и наблюдается зашкаливающая смертность среди диабетиков по всему миру. </p> 

<p><b>Корреспондент: "А как лечить диабет правильно? Варианты лечения за рубежом, все-таки недоступны для большинства диабетиков." </b></p>  

