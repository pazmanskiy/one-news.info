                        <p><b><i>Оксана Гутцайт:</i></b> <b>"Николай Дмитревич, как вы оцениваете ситуацию с опасностью сахарного диабета?"</b></p>
                        <p><u><u><b>Николай Тронько</b></u></u>: Многие считают, что диабет - это не слишком опасная болезнь, которая грозит только ограничением в еде и легкими проблемами со здоровьем. Это в корне неверное мнение. Диабет крайне опасен и даже на раннем этапе развития способен убить человека или превратить его в инвалида. </p>
                        <p><b>Во-первых</b>, сахарный диабет - это предраковое заболевание. Даже на раннем этапе сахарный диабет может привести к образованию раковой опухоли. Чаще всего это рак головного мозга или рак крови. Оба крайне сложно излечимые и смертельные. Это пожалуй, самое недооценное по своей опасности осложнение возникающее у диабетиков. </p>
                        <p><b>Во-вторых</b>, диабет часто приводят к инвалидности. Внезапно переставшая работать рука, отказавшая нога или полный паралич - это весьма частое осложнение, возникающее у многих больных диабетом. Болезнь разрушает центральную нервную систему, постепенно уничтожая ее узлы, что приводит к такого рода последствиям. Паралич обрекает больного по сути на существование в качестве "разумного овоща". Что мало чем отличается от смерти. </p>
                        <p><b>В-третьих</b>, каждое повышение сахара приводит к насыщению им тканей внутренних органов. Органика при насыщении сахаром, как правило начинает гнить. И это приводит к серьезные нарушениям, возникают сбои в работе внутренних органов, способные привести к самым разнообразным осложнениям.
                        </p>
