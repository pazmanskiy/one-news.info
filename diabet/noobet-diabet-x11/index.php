                        <p><u><u><b>Наталья Парчук</b></u></u>: Очень большое число физиков работает в сфере медицины. Я бы сказала, что половина всех прорывов в медицине прошлого века совершили именно физики. Человеческое тело - это такая же система, как и все остальные. Оно тоже состоит из атомов и молекул. Поэтому есть физики, которые занимаются изучением ядерных реакций, есть те, кто изучает планеты и галактики, а есть те, кто концентрирует свои усилия на понимании того, как работает организм человека. И как можно исправить его нарушения, в случае, если они возникают. </p>
                        <p> Я в начале карьеры выбрала именно последний путь, так как помощь живым людям мне кажется более важным, чем построение абстрактных теорий о том, как устроена вселенная. </p>
                        <p><b><i>Оксана Гутцайт:</i></b> <b>"Можете рассказать о своем открытии? Вы ведь создали методику лечения диабета?"</b></p>
                        <p><u><u><b>Наталья Парчук</b></u></u>: Причина диабета и его осложнений - это нарушения работы организма на молекулярном уровне. Если говорить грубо, то у меня получилось подобрать такой набор компонентов, которые исправляют данные нарушения и возвращают организм пациента в здоровое состояние. У него стабилизируется уровень сахара в крови, снова начинает нормально вырабатываться и усваиваться инсулин, снижается до нулевых значений риск внезапной смерти или иных осложнений диабета. После прохождения лечения, бывший диабетик становится полностю здоровым. Естественно, даже после полного излечения лучше не злоупотреблять и следить за своим здоровьем. Но факт остается фактом, бывший диабетик становится здоровым человеком.</p>
