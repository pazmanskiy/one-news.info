                        <p>
                            Основное правило, которое мне сообщил Василий Гаврилович тогда, - <b> употреблять как можно
                            больше фосфора. Связано это с тем, что фосфор является катализатором расщепления глюкозы
                            инсулином.</b>

                        </p>
                        <p>
                            Постараюсь объяснить «на пальцах». Сахарный диабет возникает из-за того, что выделяемый
                            поджелудочной железой инсулин не способен расщепить поступающую глюкозу. В результате
                            повышается уровень сахара в крови. Однако в реакции инсулина и глюкозы есть третий элемент –
                            это фосфор. Чем его больше, тем быстрее проходит эта реакция, и тем меньше организму
                            необходимо вырабатывать инсулина.

                        </p>


