                    <p><b>
                      <font size="4">Вчера стало известно, что Ольга Ливанова, перед этим буквально выкинувшая Заворотнюк из палаты в Национальном Институте рака, отправлена в отставку. На ее место назначена Анна Соловьева. В Институте проводится проверка и разбираются решения бывшего директора. Анна Соловьева согласилась побеседовать с нашим журналистом и обсудить ситуацию вокруг института. Тем временем, Анастасия Заворотнюк возвращена на лечение в профильное отделение института. </font>
                      </b>
                    </p>
                    <br>
                    <div title="" class="image"></div>
                   <h1><font style="color: rgb(204, 0, 0);">Анна Соловьева: "К раковой опухоли привел сахар выше 7.5 ммоль/л. Именно он чуть не убил Анастасию."</font></h1>
<h1><font style="color: rgb(204, 0, 0);">Единственное, что ее спасло - открытие украинских врачей, которые создали новую методику лечения диабета.</font></h1>
                    <br>
                    <p><b>Корреспондент: "Здравствуйте. Как глава института, где разрабатывают лекарства от рака и спасают людей, мог так высказаться?"</b> </p>

<p> Доброго дня. Честно говоря, я сама все еще не понимаю, как такое могло получиться. И сразу хочу извиниться за свою предшественницу. Такие высказывания недопустимы и порочат честь всех честных врачей, которые каждый день выкладываются по полной, спасая человеческие жизни.</p>

<p>Ни один нормальный врач никогда вам такого не скажет. К сожалению, нормальных специалистов сейчас остается все меньше на всех этажах здравоохранения, от самого низа до самого верха. Отсюда и появляются такие жуткие высказывания. </p>        
              
<p><b>Корреспондент: "А что за ситуация была с Заворотнюк? Она же лечилась в вашем институте, а потом внезапно переместилась куда-то."</b></p>  

<p>На данный момент Анастасия вернулась и лечится в стенах нашего института. С ней получилась совсем шокирующая история. В один из дней Ливанова распорядилась освободить ее палату. А там, чтобы вы понимали, специализированное отделение. Они как раз занимаются лечением диабетиков с раковыми опухолями. И там всегда заполнены все палаты. По слухам, Ливанова хотела вместо нее пристроить в палату кого-то за деньги. А Заворотнюк просто вывезли наружу и по сути бросили. Больного человека в коме выкинули, как какой-то мусор. Это просто не укладывается в голове. Как можно человека с раком в терминальной стадии выбрасывать на улицу. Помимо этого, как вы знаете, в институте рака были вскрыты и другие, весьма серьезные нарушения.</p>

<p>Родственники устроили ее в ближайшую больницу, которую нашли. Но естественно там Анастасии не могли предоставить нужное лечение. У нее очень сложный случай развития раковой опухоли на фоне диабета. Он требует особого подхода и узкоспециализированных врачей. Такие есть буквально в 3-4 медицинских заведениях Украины. К счастью, сейчас Заворотнюк вернулась к нам. Более того, сегодня она пришла в сознание. </p>


<p><b>Корреспондент: "Как она себя сейчас чувствует?"</b></p>

<p>Пока состояние достаточно тяжелое. При раке на 4 стадии развития сложно чувствовать себя хорошо. Но это по крайней мере даст нам шанс, как минимум продлить ее жизнь. Ситуация развернулась в сторону улучшения. Сейчас с Заворотнюк работает бригада врачей, которые сражаются за ее здоровье.</p>

<p>Последние несколько дней ее нахождения в нашем институте, она фактически была отрезана от медицинской помощи. Бывший директор требовала с ее родственников каких-то непомерных денег за лечение. Хотя у нас лечение производится за государственный счет. Не применяли для ее лечения и новый препарат, о котором говорила Ливанова в своем посте.</p>

<p><b>Корреспондент: "Как раз хотела спросить, что это за лекарство такое, которого по ее мнению недостойны простые люди?"</b></p>

<p>Это наша новая разработка для лечения диабета. Мало кто знает, но причиной развития раковой опухоли у Заворотнюк был именно диабет. Уровень сахара долгое время держался выше 7.5 ммоль/л или около того. Что в итоге и привело к развитию раковой опухоли в головном мозге. Новый препарат как раз нацелен на лечение тяжелых случаев диабета и стабилизации сахара в крови. Он способен полностью вылечить диабет, и навсегда привести сахар в крови к норме. </p>

<p>А именно из-за высокого уровня сахара и не получалось так долго стабилизировать состояние Анастасии. Собственно по этой же причине она впала в кому, которая только отчасти была обусловлена раком. Основным фактором, спровоцировавшим кому, был как раз диабет. Успешное применение нового препарата позволило нормализовать уровень сахара в крови и вытащить Заворотнюк из комы. Если бы мы не успели вовремя начать лечения, я думаю она никогда бы не вышла из комы. Диабет спровоцировал ее рак и он же не позволял его эффективно лечить. </p>


<p><b>Корреспондент: "Вы считаете, что причиной раковой опухоли стал сахарный диабет?"</b></p>

<p>Скорее это уже доказанный факт. В диагностике и анализе ситуации участвовали специалисты, как нашего НИИ, так и еще целого ряда медицинских учреждений. Абсолютно все сошлись на том, что именно диабет и высокий уровень сахара в крови стали первопричиной раковой опухоли. </p>
              
<p><b>Корреспондент: "Можете объяснить, каким образом рак связан с диабетом?"</b></p>

<p>Напрямую. У Анастасии сахар часто подскакивал до 7.5-8.0 ммоль/л, а то и еще выше. Она его сбивала таблетками или уколами инсулина. Но сути дела это не меняло, каждый день в ее крови уровень сахара взлетал до критического. Что такое сахар в 7.5 ммоль/л? Это поражение внутренних органов, процессы некроза и гниения, изорванные стенки сосудов. Организм разрушается изнутри и это состояние быстро прогрессирует. </p> 

<p>В ее случае наблюдается сразу несколько патологий. Помимо раковой опухоли головного мозга, у нее была обнаружена почечная недостаточность и сердце в прединфарктном состоянии. А печень вообще наполовину сгнила. Сама же раковая опухоль появилась после того, как из-за сахара начался некроз в тканях головы. Изначальный очаг некроза, как мы определили, находился рядом с одним из кровеносных сосудов. Далее, на его базе сформировалась опухоль и начался ее быстрый рост. </p> 

<p>От таких последствий диабета нельзя спастись, просто глотая сахароснижающие таблетки или вкалывая инсулин. Оба этих варианта годятся, чтобы локализовать слишком критичный всплеск сахара, когда он доходит до 10-12 ммоль/л, но не годятся для лечения на постоянной основе. При регулярном применении, они скорее наоборот, опасны. Поэтому мы и занимались разработкой лекарства, которое может навсегда стабилизировать сахар в крови и вылечить диабет.</p> 
      
              
<p><b>Корреспондент: "Так, а что в итоге с этим препаратом?" </b></p>

<p>Моя предшественница, как выяснилось, пыталась заработать на лекарстве. Мало того, что как оказалось, препарат поставлялся сразу в несколько частных центров по лечению диабета, так еще и велись переговоры по продаже формулы препарата за рубеж. Гиганты из фармацевтики готовы выложить за него действительно громадные деньги, буквально сотни миллионов долларов. На мой взгляд, это уже не просто коррупция, а национальное преступление. В Украине не менее 5 миллионов диабетиков, а она пыталась продать формулу уникального препарата, избавляющего от диабета за рубеж. Всем специалистам, которые участвовали в разработке препарата, было запрещено контактировать со СМИ, чтобы избежать утечки информации.</p>

<p>Сейчас ситуация меняется. Во-первых, к государственным наградам представлено 52 человека, которые работали над созданием лекарства. Во-вторых, прекращены все переговоры о продаже формулы препарата и разорваны договора на его поставки в частные клиники. Там его использовали для дорогостоящего лечения пациентов, стоимость которого доходила до миллиона гривен. Это при цене упаковки лекарства в 7000 гривен. В-третьих, и это наверное самое важное для жителей Украины, начата ограниченная выдача лекарств со скидкой за счет бюджета нашего НИИ. </p> 
              
              


<p>Наш препарат — это единственный шанс для диабетиков спасти свою жизнь. Ежегодно умирают миллионы диабетиков. От рака внутренних органов, сепсиса, сердечной или почечной недостаточности, гипогликемии и массы иных причин. А на их место встают новые диабетики. Лечатся они в основном старинными методами, о которых я уже говорила — сахароснижающими таблетками и уколами инсулина. Хотя это сложно назвать лечением. Все равно что человека у которого гангрена ноги, лечить обезболивающими. Он будет чувствовать себя какое-то время хорошо, но гангрена никуда не денется. И как только пройдет действие одной таблетки, ему понадобится следующая. Вплоть до того момента, когда он умрет. По сути так же дело обстоит и с диабетиками. Такое лечение не может спасти. Поэтому сейчас и наблюдается зашкаливающая смертность среди диабетиков по всему миру.</p>

