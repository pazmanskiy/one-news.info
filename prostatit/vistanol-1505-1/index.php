                        <h3 class="article__title"><strong>В ИЗРАИЛЕ мужчинам лечат простатит за 2-3 недели, один раз в жизни, раз и навсегда. <br>(метод описан далее)</strong></h3>
                        <div class="article__text js-mediator-article">
                            <p>
                                <font color="red"><b>Тем временем <span class="arr_textval2">в Украине</span>: жадные врачи в сговоре с аптеками скрывают эффективный препарат, который сейчас можно получить за безплатно!, а на ежегодной основе «доят деньги» из мужчин, выписывая дорогущие препараты и назначая очень дорогие процедуры! (о том где получить средство за бесплатно, внимательно читайте в статье ниже).</b></font>
                            </p>
                            <p>Простатит является самым распространенным заболеваниям среди мужчин в возрасте старше 40 лет, при этом медики отмечают неприятную тенденцию к «омоложению» заболевания. К врачам все чаще обращаются мужчины с «проблемами» в возрасте 30, 25 и даже 20 лет. </p>
                            <p style="text-align: center ! important;"><img src="img/pgum3.png" alt="" style="width: 100%; max-width: 605px;"></p>
                            <p>Популярность заболевания медики объясняют его предпосылками: любой мужчина:</p>
                            <ul>
                                <li>- ведущий малоподвижный образ жизни,</li>
                                <li>- ведущий нерегулярную половую жизнь, </li>
                                <li>- имеющий вредные привычки, </li>
                                <li>- перенесенные инфекционные заболевания,</li>
                                <li>- наследственную предрасположенность,</li>
                                <li>- перенесший стресс, болезнь или переохлаждение,</li>
                            </ul>
                            <p>практически гарантированно получает проблемы с простатой. Чем больше предпосылок — тем быстрее. Кто-то в 25 лет, кто-то в 45, это вопрос времени.</p>
                            <p class="head">Симптомы простатита — проверь себя:</p>
                            <p style="text-align: center ! important;"><img src="img/pgum2.png" alt="" style="width: 100%; max-width: 900px;"></p>
                            <p>В <span class="nowyear" data-decrease="1">2019</span> году кафедра урологии обнародовала пугающую статистику: <b>более 55% мужчин в возрасте старше 40 лет</b> сталкиваются с проблемами в области предстательной железы в той или иной форме. Определить наличие простатита довольно просто и без медицинского образования, большинство его симптомов специфичны:</p>
                            <ul>
                                <li>
                                    <b>Тянущие или режущие боли:</b>
                                    <ul>
                                        <li>- внизу живота, в мошонке; </li>
                                        <li>- в промежности или половом члене;</li>
                                    </ul>
                                </li>
                                <li>
                                    <b>Расстройство мочеиспускания:</b>
                                    <ul>
                                        <li>- учащенное мочеиспускание, жжение в уретре;</li>
                                        <li>- чувство «не до конца опорожненного мочевого пузыря»; </li>
                                        <li>- трудности с мочеиспусканием (слабая струя);</li>
                                    </ul>
                                </li>
                                <li>
                                    <b>Расстройство половой функции:</b>
                                    <ul>
                                        <li>- снижения полового влечения;</li>
                                        <li>- ухудшение длительности и качества эрекции;</li>
                                    </ul>
                                </li>
                                <li>
                                    <b>Расстройство семяизвержения:</b>
                                    <ul>
                                        <li>- преждевременная эякуляция или проблемы с ее достижением;</li>
                                        <li>- слабая эякуляция.</li>
                                    </ul>
                                </li>
                                <p> А также, повышенная утомляемость и раздражительность организма.</p>
                            </ul>
                            <p>Даже, несмотря на очевидное снижение качества жизни, многие мужчины годами живут с простатитом, не подозревая о необратимых последствиях, к которым могут привести, казалось бы, более-менее совместимые с жизнью неприятные ощущения.</p>
                            <p style="text-align: center ! important;">
                                <img src="img/adenoma-656.arb.jpg" alt="" style="width: 100%; max-width: 540px;"><br>
                                <span style="font-size: 14px;"><i>Невылеченный до конца простатит всегда приводит к аденоме простаты ("мужская смерть")</i></span>
                            </p>
                            <p>Отчасти их понять можно, диагностика простаты у любого понимающего этот процесс мужчины вызывает нешуточную неприязнь и смущение. Тем более что в подавляющем большинстве случаев после получения на руки диагноза «простатит» походы к урологу становятся ежегодной каторгой. Врачи снимают самые острые симптомы с помощью «рекомендованных фармацевтами» препаратов, однако с каждым переохлаждением или несоблюдением режима простатит «всплывает снова».</p>
                            <p class="head">Как простатит лечат у нас (и как его НЕ НУЖНО лечить)</p>
                            <p>Самое печальное то, что вы не сможете избавиться от простатита, даже если очень сильно захотите. </p>
                            <p>Стандартная процедура лечения простатита:</p>
                            <p style="text-align: center ! important;"><img src="img/pgum1.png" alt="" style="width: 100%; max-width: 605px;"></p>
                            <ul>
                                <li>Мужчина обращается в клинику. <b>Платную или бесплатную – не имеет значения, поскольку в итоге платить придется все равно.</b></li>
                                <li>Врач проводит обследование, назначает кучу анализов. Некоторые даже не нужны, но на всякий случай, вдруг у человека найдется что-то еще, что можно вылечить. Плюс сами анализы стоят денег.</li>
                                <li>После обследования врач ставит диагноз «простатит» и прописывает «рекомендуемые препараты». Препараты прописываются те, которые предназначены для снятия острых симптомов заболевания, но не лечения хронического простатита. <b>И конечно же уролог рекомендует препараты компаний, чьи медицинские представители занесли ему больше всего денег. Препараты эти у всех на слуху.</b></li>
                                <li>
                                    В дополнении к “рекомендуемым” препаратам для снятия симптомов врачи всегда прописывают ректальный массаж простаты или же аппаратное лечение схожего действия. Это унизительная и очень неприятная процедура – массаж делается пальцем через анальное отверстие мужчины. В среднем массаж подразумевает 10-14 сеансов. За каждый сеанс, конечно, приходится платить. <b>В Европе данный массаж не делают уже более 20 лет, так как современные препараты позволяют вылечить простатит без него!</b><br>
                                </li>
                                <li>Помимо основного лечения врачи часто прописывают препараты для улучшения сексуальных функций, повышения качества спермы, «восстановления организма» после антибиотиков и т.д.</li>
                            </ul>
                            <p><b>В итоге единоразово лечение простатита <span class="arr_textval2">в Украине</span> обойдется мужчине от 350 до 800 ГРН, урологи действительно формируют курс терапии исходя из финансовых возможностей пациента в первую очередь.</b> При этом будут сняты только основные острые симптомы заболевания. Хронический же простатит останется и проявит себя снова, как только мужчина переохладится, или перестанет соблюдать назначенный врачом режим питания. В результате <b>указанные суммы приходится платить ежегодно</b> — именно на этом построен совместный уролого-фармацевтический бизнес.</p>
                            <p class="head" id="fade-in-from-here">Как простатит лечат еврейские мужчины (как его НУЖНО лечить)</p>
                            <p><b>В Израиле, чтобы вылечить простатит, не надо обращаться даже к врачу.</b> Достаточно при первых симптомах пойти в аптеку и купить десятилетиями проверенное средство, которые не доходят до рынка <span class="arr_textval1">Украине</span> лишь потому, что они помогают вылечить хронический простатит полностью (раз в жизни и навсегда)! Естественно, такой препарат не выгоден украинской аптечной мафии – ведь намного выгоднее «подсадить» человека на ежегодное снятие болевых симптомов, чем вылечить заболевание полностью недорогим и доступным каждому человеку препаратом.</p>
                            <p>В отличие от нашей страны в Израиле сильно развито страхование. Страховые компании не заинтересованы в том, чтобы каждый второй мужчина посещал врачей 2 раза в год — ведь компании вынуждены все это оплачивать, а зарплаты врачей там немаленькие. Наоборот, они заинтересованы в том, чтобы лечение было максимально эффективным и быстрым. Раз и навсегда.<br>В Израиле интересы страховых компаний победили аптечную мафию и, к счастью для рядовых мужчин, рабочее средство для лечения простатита всегда имеется в продаже...</p>
                            <p class="head">Изменится ли ситуация <span class="arr_textval2">в Украине</span> к лучшему?</p>
                            <p>Каких-либо положительных изменений в сфере работы украинских аптек и врачей, к сожалению, ожидать не приходится – ведь фармакологический бизнес многомиллиардный, и доход с него получают самые высокие чины. </p>
